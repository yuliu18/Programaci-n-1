#include <stdio.h>

int main()
{
    int num;
    printf("Introduzca un numero: ");
    scanf(" %d", &num);

    for (int f = 0; f < num; f++)
    {
        for (int c = 0; c <= f; c++)
        {
            if ((f == num - 1) || (c == 0) || (c == f))
                printf("*");
            else
                printf(" ");
        }
        printf("\n");
    }
}
